num = int(input("숫자를 입력하세요 :"))

if 3<=num<=5:
    print("%d은 봄이다" %num)
elif 6<=num<=8:
    print("%d은 여름이다" %num)
elif 9<=num<=11:
    print("%d은 가을이다" %num)

else:
    print("%d은 겨울이다" %num)

