g = input("등급(대문자)을 입력하세요")

if g == "A+":
    print("등급:%s,평점은 4.5" %g)
if g == "A":
    print("등급:%s,평점은 4.0" %g)
if g == "B+":
    print("등급:%s,평점은 3.5" %g)
if g == "B":
    print("등급:%s,평점은 3.0" %g)
if g == "C+":
    print("등급:%s,평점은 2.5" %g)
if g == "C":
    print("등급:%s,평점은 2.0" %g)
if g == "D+":
    print("등급:%s,평점은 1.5" %g)
if g == "D":
    print("등급:%s,평점은 1.0" %g)
if g == "F":
    print("등급:%s,평점은 0" %g)
else:
    print("등급을 잘못 입력 했습니다.")